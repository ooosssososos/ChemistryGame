public class Vector3f {

    public float x = 0.0f;
    public float y = 0.0f;
    public float z = 0.0f;

    public Vector3f(float x, float y, float z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Vector3f(){ }

}
